package com.store.db;

public class TimeSaveBean {
	
	private int class_registrynum;
	private String class_date;
	private String class_day;
	private String class_starttime;
	private String class_endtime;
	
	public int getClass_registrynum() {
		return class_registrynum;
	}
	public void setClass_registrynum(int class_registrynum) {
		this.class_registrynum = class_registrynum;
	}
	public String getClass_date() {
		return class_date;
	}
	public void setClass_date(String class_date) {
		this.class_date = class_date;
	}
	public String getClass_day() {
		return class_day;
	}
	public void setClass_day(String class_day) {
		this.class_day = class_day;
	}
	public String getClass_starttime() {
		return class_starttime;
	}
	public void setClass_starttime(String class_starttime) {
		this.class_starttime = class_starttime;
	}
	public String getClass_endtime() {
		return class_endtime;
	}
	public void setClass_endtime(String class_endtime) {
		this.class_endtime = class_endtime;
	}
	
	
	
}
